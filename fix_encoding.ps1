# 获取所有包含问号的Java文件
$files = git grep -l '?' -- '*.java'

$fixedCount = 0
$totalCount = $files.Count

foreach ($file in $files) {
    $filePath = Join-Path $PWD $file
    
    if (Test-Path $filePath) {
        $content = Get-Content $filePath -Raw -Encoding UTF8
        $originalContent = $content
        
        # 按长度排序，先替换长的模式
        $sortedKeys = $correctionMap.Keys | Sort-Object -Property Length -Descending
        
        foreach ($pattern in $sortedKeys) {
            $replacement = $correctionMap[$pattern]
            $content = $content -replace [regex]::Escape($pattern), $replacement
        }
        
        # 处理剩余的单独问号（在中文上下文中）
        # 中文字符 + ? + 中文字符
        $content = $content -replace '([\u4e00-\u9fa5])\?([\u4e00-\u9fa5])', '$1$2'
        # 行尾的问号（中文注释）
        $content = $content -replace '([\u4e00-\u9fa5])\?\s*\n', '$1\n'
        $content = $content -replace '([\u4e00-\u9fa5])\?\s*\*', '$1 *'
        
        if ($originalContent -ne $content) {
            [System.IO.File]::WriteAllText($filePath, $content, [System.Text.Encoding]::UTF8)
            $fixedCount++
            Write-Host "✓ 已修复: $file" -ForegroundColor Green
        }
    } else {
        Write-Host "✗ 文件不存在: $file" -ForegroundColor Red
    }
}

Write-Host "`n总计修复 $fixedCount/$totalCount 个文件" -ForegroundColor Cyan
